    if (newProvince) {
        const districtList = districts[newProvince] || [];
        options.value[1] = districtList.map(district => ({
            label: district,
            value: district
        }));
        selections.value[1] = ''; // 시/군/구 선택 초기화
    }
});

const nextStep = async () => {
    // 날짜 형식으로 변환
    const [province, district] = selections.value;
    const address = [province, district];

    const { data: { session }, error: sessionError } = await supabase.auth.getSession(); // 현재 세션 정보 가져오기

    if (sessionError || !session || !session.user) {
        console.error('세션 정보가 유효하지 않습니다.');
        return; // 세션 정보가 없으면 함수 종료
    }

    const userId = session.user.id; // UID 가져오기

    const { error } = await supabase
        .from('users')
        .update({ address }) // 생일 업데이트
        .eq('id', userId); // UID로 조건 설정

    if (error) {
        console.error('데이터 업데이트 오류:', error);
    } else {
        emit('nextStep'); // 이벤트 호출
